     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $cource = Course::find($id);
        $cource->name = $request->name;
        $cource->is_active = 1;
        $cource->save();

        return redirect()->route('course.index');
    }

